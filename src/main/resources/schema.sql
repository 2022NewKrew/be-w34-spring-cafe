DROP TABLE IF EXISTS USERS;
DROP TABLE IF EXISTS ARTICLES;

CREATE TABLE USERS
(
    ID       BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    USERID   VARCHAR(255),
    PASSWORD VARCHAR(255),
    NAME     VARCHAR(255),
    EMAIL    VARCHAR(255),
    PRIMARY KEY (ID)
);

CREATE TABLE ARTICLES
(
    ID         BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    AUTHOR     VARCHAR(255),
    TITLE      VARCHAR(255),
    CONTENT    VARCHAR(255),
    CREATEDATE DATETIME DEFAULT CURRENT_TIMESTAMP(),
    PRIMARY KEY (ID)
);
