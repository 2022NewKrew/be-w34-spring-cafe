DROP TABLE IF EXISTS POST;
DROP TABLE IF EXISTS USERS;

CREATE TABLE IF NOT EXISTS POST
(
    ID      LONG PRIMARY KEY,
    TITLE   VARCHAR(100),
    CONTENT LONGTEXT
);

CREATE TABLE IF NOT EXISTS USERS
(
    ID       LONG PRIMARY KEY,
    USERID   VARCHAR(30),
    NICKNAME VARCHAR(30),
    EMAIL    VARCHAR(30),
    PASSWORD VARCHAR(30)
);
