DROP TABLE IF EXISTS ARTICLE;
CREATE TABLE IF NOT EXISTS ARTICLE (
    ARTICLE_ID INT,
    WRITER VARCHAR(32),
    TITLE VARCHAR(100),
    CONTENTS VARCHAR(1000),
    CREATETIME TIMESTAMP,
    PRIMARY KEY (ARTICLE_ID)
                                   );

DROP TABLE IF EXISTS USERS;
CREATE TABLE IF NOT EXISTS USERS(
    ID INT,
    USERID VARCHAR(32),
    PASSWORD VARCHAR(32),
    USERNAME VARCHAR(32),
    EMAIL VARCHAR(64),
    PRIMARY KEY (USERID)
                                );