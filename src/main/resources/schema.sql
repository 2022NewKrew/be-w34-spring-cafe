drop table if exists articles;
drop table if exists members;

create table articles (
    id bigint generated by default as identity,
    title varchar(255),
    content varchar(255),
    primary key (id)
);
create table members (
    id bigint generated by default as identity,
    uid varchar(255),
    password varchar(255),
    name varchar(255),
    email varchar(255),
    content varchar(255),
    primary key (id)
);
