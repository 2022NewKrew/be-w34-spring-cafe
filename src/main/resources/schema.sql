drop table USER if exists;
drop table ARTICLE if exists;

create table USER
(
    id       bigint generated by default as identity,
    userid  varchar(255),
    password varchar(255),
    name     varchar(255),
    email   varchar(255),
    PRIMARY KEY (id)
);

create table ARTICLE
(
    id       bigint generated by default as identity,
    writer   varchar(255),
    title    varchar(255),
    contents varchar(255),
    PRIMARY KEY (id)
);