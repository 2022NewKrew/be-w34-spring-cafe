DROP TABLE IF EXISTS USERLIST;
CREATE TABLE IF NOT EXISTS USERLIST
(
    ID       LONG PRIMARY KEY,
    USERID   VARCHAR(32),
    PASSWORD VARCHAR(32),
    NAME     VARCHAR(32),
    EMAIL    VARCHAR(32)
);

DROP TABLE IF EXISTS ARTICLE;
CREATE TABLE IF NOT EXISTS ARTICLE
(
    ID        LONG PRIMARY KEY,
    WRITETIME VARCHAR(32),
    WRITER    VARCHAR(32),
    TITLE     VARCHAR(32),
    CONTENTS  VARCHAR(100)
);
